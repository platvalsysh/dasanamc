export interface CheckRow {
  item: string;
  head?: boolean;
  sep?: boolean;
  b: string;
  s: string;
  p: string;
}

interface CheckGroup {
  rows: { item: string; b: string; s: string; p: string }[];
}

function buildRows(groups: readonly CheckGroup[]): CheckRow[] {
  const mark = (v: string) => (v === "✓" ? "✓" : v ? v : "·");
  const out: CheckRow[] = [];
  groups.forEach((g, gi) => {
    g.rows.forEach((r, ri) => {
      out.push({
        item: r.item,
        b: mark(r.b),
        s: mark(r.s),
        p: mark(r.p),
        head: ri === 0,
        sep: ri === 0 && gi > 0,
      });
    });
  });
  return out;
}

